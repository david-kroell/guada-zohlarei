package backslash.at.smartbank;

public class Bill {
    public int Id;
    public String Description;
    public Double Price;
    public String Image;
    public String Title;

    public String getDescription() {
        return Description;
    }

    public Double getPrice() {
        return Price;
    }

    public String getImage() {
        return Image;
    }

    public String getTitle() {
        return Title;
    }
}
